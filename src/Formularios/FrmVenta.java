    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        Reserva reserva =
            DatosSistemas.gestionReserva.getReserva(
                    cboReserva.getSelectedIndex());

    Venta venta = new Venta(
            reserva,
            cboModalidad.getSelectedItem().toString(),
            Double.parseDouble(txtPrecio.getText()));

    if (DatosSistemas.gestionVenta.registrar(venta)) {

        if (cboModalidad.getSelectedItem().toString().equalsIgnoreCase("Cuotas Directas")) {

            venta.setCuotaInicial(
                    Double.parseDouble(txtCuotaInicial.getText()));

            venta.setNumeroCuotas(
                    Integer.parseInt(txtNumeroCuotas.getText()));

            venta.setMontoCuota(
                    Double.parseDouble(txtMontoCuota.getText()));

        }

        JOptionPane.showMessageDialog(this,
                "Venta registrada correctamente.");
        generarContrato(venta);
        limpiar();
        cargarReservas();
    } else {

        JOptionPane.showMessageDialog(this,
                "No se pudo registrar la venta.");

    }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        Venta venta = DatosSistemas.gestionVenta.buscar(
            cboReserva.getSelectedItem().toString());

    if (venta != null) {

        cboModalidad.setSelectedItem(
                venta.getModalidadPago());

        txtPrecio.setText(
                String.valueOf(venta.getPrecioTotal()));

        txtCuotaInicial.setText(
                String.valueOf(venta.getCuotaInicial()));

        txtNumeroCuotas.setText(
                String.valueOf(venta.getNumeroCuotas()));

        txtMontoCuota.setText(
                String.valueOf(venta.getMontoCuota()));

    } else {

        JOptionPane.showMessageDialog(this,
                "Venta no encontrada.");

    }

    }//GEN-LAST:event_btnBuscarActionPerformed

