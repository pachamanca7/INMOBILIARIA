    public void cargarReservas() {

    cboReserva.removeAllItems();

    for (int i = 0;
            i < DatosSistemas.gestionReserva.getCantidad();
            i++) {

        Reserva reserva =
                DatosSistemas.gestionReserva.getReserva(i);

        if (reserva.getDepartamento().getEstado().equalsIgnoreCase("Reservado")) {

            cboReserva.addItem(
                    reserva.getDepartamento().getCodigo());

        }

    }

    }
    
