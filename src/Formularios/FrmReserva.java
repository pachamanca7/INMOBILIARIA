    public void cargarClientes() {
        cboCliente.removeAllItems();
        for (int i = 0;
                i < DatosSistemas.gestionCliente.getCantidad();
                i++) {
            Cliente cliente =
                    DatosSistemas.gestionCliente.getCliente(i);
            cboCliente.addItem(cliente.getDni());
        }
    }
    
    public void cargarDepartamentos() {
        cboDepartamento.removeAllItems();
        for (int i = 0;
                i < DatosSistemas.gestionDepartamento.getCantidad();
                i++) {
            Departamento departamento =
                    DatosSistemas.gestionDepartamento.getDepartamento(i);
            if (departamento.getEstado().equalsIgnoreCase("Disponible")) {
                cboDepartamento.addItem(departamento.getCodigo());
            }
        }
    }
