    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        Cliente cliente =
            DatosSistemas.gestionCliente.getCliente(
                    cboCliente.getSelectedIndex());

    Departamento departamento =
            DatosSistemas.gestionDepartamento.getDepartamento(
                    cboDepartamento.getSelectedIndex());

    Reserva reserva = new Reserva(
            cliente,
            departamento,
            Double.parseDouble(txtMonto.getText()),
            txtFechaReserva.getText(),
            txtFechaVencimiento.getText());

    if (DatosSistemas.gestionReserva.registrar(reserva)) {

        JOptionPane.showMessageDialog(this,
                "Reserva registrada correctamente.");

        limpiar();

        cargarDepartamentos();

    } else {

        JOptionPane.showMessageDialog(this,
                "El departamento ya no está disponible.");

    }

    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        Reserva reserva = DatosSistemas.gestionReserva.buscar(
            cboDepartamento.getSelectedItem().toString());

    if (reserva != null) {

        cboCliente.setSelectedItem(
                reserva.getCliente().getDni());

        txtMonto.setText(
                String.valueOf(reserva.getMontoSeparacion()));

        txtFechaReserva.setText(
                reserva.getFechaReserva());

        txtFechaVencimiento.setText(
                reserva.getFechaVencimiento());

    } else {

        JOptionPane.showMessageDialog(this,
                "Reserva no encontrada.");

    }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (DatosSistemas.gestionReserva.eliminar(
            cboDepartamento.getSelectedItem().toString())) {

        JOptionPane.showMessageDialog(this,
                "Reserva eliminada.");

        limpiar();

        cargarDepartamentos();

    } else {

        JOptionPane.showMessageDialog(this,
                "Reserva no encontrada.");

    }
    }//GEN-LAST:event_btnEliminarActionPerformed

